package com.nt.javalang;

public class CustomToStringTest {
	public String toString() {
		return "CustomToStringTest";
	}

	public static void main(String[] args) {
		Integer integer = new Integer(10);
		String string = new String("RAJ");
		CustomToStringTest customToStringTest = new CustomToStringTest();
		System.out.println(integer);
		System.out.println(string);
		System.out.println(customToStringTest);
	}
}
