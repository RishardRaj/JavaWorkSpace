package com.nt.exception;

public class DefaultExceptionHandlerTest {
	public static void main(String[] args) {

		try {
			System.out.println(10 / 0);
		} catch (ArithmeticException e) {
			System.out.println(10 / 0);
		} finally {
			String s = null;
			System.out.println(s.length());
		}
	}

}
